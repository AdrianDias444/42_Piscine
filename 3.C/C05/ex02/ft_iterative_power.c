#include <stdio.h>

int	ft_iterative_power(int nb, int power)
{
	int	nb_inicial;

	nb_inicial = nb;
	if (power < 0)
		return (0);
	if (power == 0)
		return (1);
	while (power > 1)
	{
		nb = nb * nb_inicial;
		power--;
	}
	return (nb);
}
