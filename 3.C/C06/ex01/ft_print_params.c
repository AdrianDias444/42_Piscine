#include <unistd.h>

int	main(int argc, char **argv)
{
	int		i;
	int		c;
	char	*str;

	c = 1;
	while (argc - 1 >= c)
	{
		i = 0;
		str = argv[c];
		while (str[i] != '\0')
		{
			write(1, &str[i], 1);
			i++;
		}
		write(1, "\n", 1);
		c++;
	}
}
