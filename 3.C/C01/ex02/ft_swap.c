#include <unistd.h>

void	ft_swap(int *a,	int *b)
{
	int	temp;

	temp = *a;
	*a = *b;
	*b = temp;
}


int	main(void)
{
	int	a;
	int	b;

	a = 42;
	b = 21;
	ft_swap(&a, &b);
}
