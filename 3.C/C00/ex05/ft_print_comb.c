#include<unistd.h>

void	ft_print_comb(void)
{
	int a;
	a = 0;

	int b;
	b = 1;

	int c;
	c = 2;

	while (c <9)
	{
		write(1, &a, 1);
		write(1, &b,1);
		write(1, &c,1);
		
		c++;
	}
	

}

int	main(void)
{
	ft_print_comb();
}